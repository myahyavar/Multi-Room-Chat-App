    private void btnSendRActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendRActionPerformed

        String chatmsg = txtMessageR.getText();

        Message msg = new Message(Message.Message_Type.RoomMSG);
        ArrayList<String> elements = new ArrayList<String>();
        elements.add(lblRoomName.getText());
        elements.add(username + ": " + chatmsg);
        msg.content = elements;
        Client.Send(msg);

    }//GEN-LAST:event_btnSendRActionPerformed

