    private void btnChooseFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChooseFActionPerformed

        JFileChooser chooser = new JFileChooser();
        chooser.setDialogTitle("Select a File");
        if (chooser.showOpenDialog(null) == JFileChooser.APPROVE_OPTION) {
            myFile = chooser.getSelectedFile();
            lblfname.setText(myFile.getName());
            lblfname.setVisible(true);

        }
    }//GEN-LAST:event_btnChooseFActionPerformed

    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed

        if (myFile == null) {
        JOptionPane.showMessageDialog(null, "Please select a file.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                Message msg = new Message(Message.Message_Type.File);
                ArrayList items = new ArrayList<>();
                items.add(roomName);
                items.add(username);
                items.add(myFile.getName());
                byte[] content = Files.readAllBytes(myFile.toPath());
                items.add(content);

                msg.content = items;
                Client.Send(msg);
                myFile = null;
                lblfname.setText("");
                lblfname.setVisible(false);
            } catch (IOException ex) {
                Logger.getLogger(Room.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnSendActionPerformed

